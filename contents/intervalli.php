<br><b><font size=5>B</font></b>asilarmente l'<i>armonia</i> si occupa di studiare quali successioni di note e intervalli risultino gradevoli o sgradevoli all'orecchio e comunque quale effetto producano.<br>
Qui' riportiamo brevemente quali siono le note che suonate contemporaneamente producono una <i>assonanza</i>, cioè un effetto "gradevole" all'orecchio e quali invece producono una <i>dissonanza</i>, cioè un effetto "sgradevole" e "aspro".<br>
<br>
<li> Sono consonanti gli intervalli di <u>ottava giusta</u>, <u>quinta giusta</u>, <u>terza maggiore</u>, e <u>terza minore</u>.
<li> Gli intervalli di <u>quarta</u> e <u>sesta</u> sono dissonanti se formano intervallo di <u>seconda</u> con altre note, altrimenti sono consonanti.

<li> Sono dissonanti gli intervalli di <u>seconda</u>, <u>settima</u> e tutti gli altri intervalli ( diminuiti eccedenti etc.. ).
<br><br>
Ad esempio prendiamo la Scala Maggiore di Do: Do Re Mi Fa Sol La Si Do, prendendo le note Do e Sol e suonandole contemporaneamente sappiamo che il risultato sarà gradevole all'orecchio poichè tra di essi passa una quinta giusta. Se prendiamo le note Mi e Fa e le suoniamo otterremo un effetto "aspro" e "instabile".
