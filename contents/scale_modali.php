<br><font size=5>Q</font>uesti nomi derivano dalle antiche scale greche (solo i nomi) ed erano in uso già nel medioevo sotto il nome di scale gregoriane. Oggi queste scale continuano ad essere usate, esse permettono infatti di dare un senso leggermente diverso al sound dei vari fraseggi.
Esse hanno il pregio di essere ottenute senza impiego di alterazioni a partire da una stessa scala "madre" e perciò i nostri BOX rimangono gli stessi, solo che volta per volta cambia la nota che consideriamo tonica.
<br><font size=5>N</font>aturalmente non basta "teorizzare" il modo in cui stiamo suonando, sta alla sensibilità del musicista far sentire all'orecchio che quel dato fraseggio è in una particolare scala modale, indugiando maggiormente sulle note principali di quella scala (I IV V grado) ad esempio.
Ma questo è un discorso molto complesso che spero di approfondire in futuro. 
Comunque penso che la cosa migliore sia suonare, sperimentare ed esercitare il più possibile la nostra creatività, questo è il tipo di esperienza che sicuramente ci arricchirà di più.

<br><font size=5>Q</font>ueste tabelle torneranno particolarmente utili quando parleremo dell' armonizzazione delle scale.
