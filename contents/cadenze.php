<font size=5>S</font>i chiama cadenza la successione di accordi che concludono un inciso, una frase o un brano.<br>
Esse vengono classificate a seconda dell'idea di conclusione più o meno accentuata che trasmettono. Esse possono essere dette.<br>
<FONT SIZE=-1>
<li type=square>CONCLUSIVE
<li type=square>SEMICONCLUSIVE
<li type=square>NON CONCLUSIVE
</FONT>
<br><br>
<font size=5>C</font>he a loro volta si dividono in:<br>
<!FONT SIZE=-1>
<ol type=square>
<li>CONCLUSIVA

<ol type=square>
<li>PERFETTA: quando si passa da un accordo costruito sul V ad uno costruito sul I grado.<br>
Ad es. Sol ==> Do
<li>PLAGALE: quando si passa da un accordo costruito sul IV ad uno costruito sul I grado.<br>
Ad es. Fa ==> Do
</ol><br>


<li>SEMICONCLUSIVA

<ol type=square>
<li>IMPERFETTA: quando si passa da un accordo costruito sul V ad uno costruito sul I grado e almeno uno dei due accordi è allo stato di rivolto.<br>
</ol>	<br>

<li>NON CONCLUSIVA

<ol type=square>
<li>SOSPESA: quando si passa da un accordo qualunque ad uno costruito sul V grado.<br>
Ad es. Rem ==> Sol
<li>D'INGANNO: quando si passa da un accordo costruito sul V ad uno costruito su un grado diverso dal I.<br>
<li>EVITATA: quando si passa da un accordo costruito sul V ad uno costruito su un'altra tonalità.<br>
Ad es. Sol (tonalità di Do MAggiore) ==> Re (tonalità di Sol Maggiore).
</ol><br>

</ol>	
<!/FONT>
<font size=5>N</font>ota che nelle cadenze viste sopra possono essere coinvolti anche accordi di settima o nona etc.
