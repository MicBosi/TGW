<p>Scegliere con quale chitarra cominciare a suonare e' la prima scelta importante che un aspirante chitarrista si trova a fare. 
Tipicamente la scelta e' fra le seguenti tre:</p>

<p>Chitarra Classica<br>
<img src="images/classic-guitar.jpg">
</p>

<p>Chitarra Acustica<br> 
<img src="images/acustic-guitar.jpg">
</p>

<p>Chitarra Elettrica<br>
<img src="images/electric-guitar.jpg">
</p>

<p>La musica e' una questione di emozioni, per cui secondo me bisogna iniziare a suonare lo strumento per il quale abbiamo deciso di imparare, cioè quello che ci ha ispirati a cominciare.
Questa domanda si pone soprattutto per coloro che vogliono suonare la chitarra elettrica i quali spesso si sentono dire che è meglio cominciare con la chitarra classica per poi passare a quella elettrica solo dopo avere acquisito solide basi.</p>

<p>Personalmente penso che questa sia una semplice perdita di tempo e di soldi. Inoltre quando avrai guadagnato sufficiente destrezza sulla tua chitarra preferita ti verra' naturale passare da un tipo all'altro in base all'esigenza o ispirazione del momento.</p>

<p>Per cominciare, la più difficile è senza ombra di dubbio la chitarra acustica, questo per il semplice fatto che le sue corde sono di gran lunga più rigide della chitarra classica e di quella elettrica. Ciò puo' creare nelle prime fasi qualche problema visto che i polpastrelli dovranno sopportare uno sforzo maggiore e gli accordi col barrè risulteranno più difficili. La chitarra elettrica è quella con le corde più morbide, per cui questi problemi si faranno sentire in misura minore. La chitarra classica come difficoltà iniziali cade più o meno tra queste due.</p>

<p>Se sei un mancino e stai pensando di comprare una chitarra mancina forse puo' essere una buona idea provare per un paio di mesi a suonare con con una chitarra "normale" destra, magari prestata da un amico. Non si sa mai che ti ci trovi bene. Questo ti dara' una maggiore liberta' di scelta e forse risparmierai anche qualche soldino quando sara' il momento di comprare la tua prossima chitarra.</p>

<p>Qualunque sia la tua decisione sappi che ogni chitarrista, a qualsiasi livello, deve affrontare varie difficoltà lungo il suo percorso. Per cui non perderti d'animo se all'inizio tutto sembrera' difficile e misterioso, vedrai con la giusta pratica e un po di pazienza supererai ogni ostacolo.</p>

