<p>Se dovessi cominciare oggi a suonare la chitarra questo e' il percorso di studio che a grandi linee cercherei di seguire:</p>

<ul>
	<li>Imparare l'anatomia e la terminologia della chitarra: capotasto, ponte, buca, chiavette, i nomi delle corde, etc.</li>
	<li>Come accordare la chitarra sia ad orecchio che con l'accordatore elettronico.</li>
	<li>Come si cambiano le corde in una chitarra classica, folk ed elettrica.</li>
	<li>Plettri: presentazione dei vari tipi, forme e materiali.</li>
	<li>I primissimi accordi: Do maggiore, Sol maggiore, Re maggiore/minore, Mi maggiore/minore, La maggiore/minore, Fa maggiore/minore.</li>
	<li>Giro armonico di Do, di Sol e di Re (lascia perdere le settime per il momento).</li>
	<li>Giro armonico di La, di Mi e di Fa (lascia perdere le settime per il momento).</li>
	<li>Ritmo con plettrata o dita: 2 o 3 tipi base.</li>
	<li>Arpeggi con le dita: 2 o 3 tipi base.</li>
	<li>Prendersi cura dei polpastrelli e delle unghie.</li>
	<li>Ritmo con plettrata o dita: altri 2 o 3 tipi base.</li>
	<li>Arpeggi con le dita: altri 2 o 3 tipi base.</li>
	<li>Accordi: pratica piu' approfondita di tutti gli accordi base e dei vari cambi.</li>
	<li>Accordi: pratica attraverso canzoni a piacere.</li>
	<li>Accordi barre': pratica di tutti gli accordi barre' e dei vari cambi.</li>
	<li>Accordi barre': pratica attraverso canzoni a piacere.</li>
	<li>Accordi 7a: posizioni degli accordi di 7a principali.</li>
	<li>Accordi 7a: giri armonici di Do, Sol, Re, La, Mi e Fa con le settime.</li>
	<li>Accordi 7a: pratica attraverso canzoni a piacere.</li>
	<li>Accordi: approfondimento su tutti gli altri accordi</li>
	<li>I Giri Armonici: schema di tutti i giri armonici su tutte le tonalita'.</li>
	<li>Progressioni e cadenze tipiche nelle principali tonalita'.</li>
	<li>Chitarra ritmica: ritmi blues, funky, raggae, metal e atri pattern ritmici.</li>
	<li>Chitarra ritmica: pratica attraverso canzoni a piacere.</li>
