<br><i>ATTENZIONE: il contenuto di questa sezione <u>non è legge né oro colato</u> è solamente il mio tentativo personale di ordinare in qualche modo l'immenso materiale che un bravo chitarrista, sempre secondo me, dovrebbe conoscere.
<br>Non è detto che tu la debba pensare come me e in questo caso non esitare a spedirmi una email, condividere le idee è il modo migliore e più veloce per migliorarle!</i>
<br>

<br><b><font size=5>I</font></b> contenuti del sito sono essenzialmente di tre tipi:
<br>
<ol type=I><li>Concetti Teorici.
<li>Conoscenze Pratiche.
<li>Metodi Di Applicazione.</ol>
<br><b><font size=5>C</font></b>oncetti teorici.
<br>A questa categoria appartengono:
<li>La lettura della notazione musicale classica (pentagramma, chiavi, note, pause, segni dinamici etc.), dei tablature, degli schemi degli accordi.
<li>La teoria generale dei toni/semitoni, costruzione delle scale, vari tipi di scale, tonalità e rapporti tra le scale, ritmi e tempi.
<li>Armonia: consonanze e dissonanze, armonici, triadi, armonizzazioni delle scale, vari tipi di accordi, rapporti tra i vari tipi di accordi e tra accordi e scale.
<br>
<br><b><font size=5>C</font></b>onoscenze Pratiche.
<br>Questa categoria comprende:
<li>Vari tipi di diteggiature delle varie scale.
<li>Diteggiature delle diverse triadi e quadriadi etc.
<li>Diteggiature degli accordi.
<li>Posizioni degli armonici, delle note sulla tastiera, degli intervalli.
<li>Ear-Training (totalmente mancante).
<br>
<br><b><font size=5>A</font></b>pplicazioni.
<br>In questa categoria troviamo:
<li>Pattern.
<li>Licks e fraseggi.
<li>Studio delle varie tecniche: tapping, sweep-picking, armonici, string skipping, etc.
<li>Accompagnamenti vari per l'improvvisazione.
<br>


<br><b><font size=5>L</font></b>o schemetto rappresentato qua sopra rappresenta a grandi linee gran parte del contenuto del sito (la sezione con gli accompagnamenti per le improvvisazioni, l'ear-training, una parte della lettura musicale, alcune parti, molto interessanti, dell'armonia, nonchè lo studio specifico delle tecniche quali sweep, tapping etc. purtroppo non sono ancora state comprese).
<br>Per ora l'unico consiglio che posso darti per orientarti meglio e decidere cosa studiare è questo: <i> devi assolutamente chiederti cosa vuoi diventare e quali capacità vuoi sviluppare maggiormente</i>. 
<br>Quando ti sarai dato una risposta, il che non è affatto facile, saprai quali sono le cose che ti interessano e quali no.
<br>Personalmente, in caso di indecisione, ti consiglio di privilegiare l'aspetto pratico, ovvero, impara nuovi accordi ed esercitati con impegno, impara un box, poi un'altro, poi una scala, fai pratica, improvvisa, mescola accordi e scale, cerca di ricordare le toniche e le note dell'accordo di tonica, cura la precisione e la purezza del suono, l'eleganza del movimento, crea.
