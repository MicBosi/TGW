Spesso si trovano gli accordi scritti in tablatura, ad esempio:
<pre>
    DO    RE    SOL   FA
||--0-----2-----3-----1-----||
||--1-----3-----0-----1-----||
||--0-----2-----0-----2-----||
||--2-----0-----0-----3-----||
||--3-----X-----2-----3-----||
||--X-----X-----3-----1-----||
</pre>
In questo caso i numeri NON indicano le dita ma il tasto della chitarra da premere.
Una eventuale X indica che tale corda non va suonata. Il numero 0 indica di suonare una
determinata corda senza premere alcun tasto.
