<font size=5>A</font>bbiamo detto che la triade èun insieme di tre note ordinate per terze e abbiamo anche detto che poichè si dice <i>accordo</i> l'unione di due o piùsuoni ordinati per terze, allora anche la triade è un accordo.<br>
Ma cosa succede se prendiamo le note di un accordo e le disponiamo non più su di un'unica ottava ma su differenti ottave?<br>

<br><img src="fragments/triade allargata.gif" Alt="Triade allargata di Do non raddoppiata"><br><br>
<font size=5>L</font>'accordo che è nella figura sopra è detto accordo <i>allargato</i> in quanto tra il suo suono più grave e quello più acuto esistono delle note facenti parte dell'accordo che non compaiono. Detto in altre parole l'accordo "salta" delle note che gli apparterrebbero.<br>

<br><font size=5>Q</font>uando invece un accordo presenta dei suoni raddoppiati viene detto <i>esteso</i>. Questo naturalmente puo' accadere solo nel caso in cui l'accordo abbracci più di un'ottava come in figura sotto.<br>

<br><img src="fragments/triade estesa.gif" Alt="Triade estesa di Do non allargata"><br><br>
<font size=5>N</font>aturalmente puo' accadere che i due casi visti in precedenza si combinino, in questo caso il nostro accordo viene semplicemente detto <i>allargato esteso</i> o <i>esteso allargato</i>, come nella figura sotto.<br>

