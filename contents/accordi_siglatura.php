<font size=5>Q</font>uali regole stanno dietro a queste sigle?<br>
<ol type=circle>
<li>Il nome, ad esempio DO, ci indica la fondamentale dell'accordo, quando non ci sono ulteriori specifiche si sottintende che con la fondamentale è presente la terza maggiore e la quinta giusta.<br>

<br><li> Una "m" subito dopo ci indica che è minore, ad esempio DOm.<br>

<br><li>Un "7" indica che è presente la settima minore. <br>

<br><li>Un "maj7" o "maj" indica che è presente la settima maggiore.<br>

<br><li> Un "9" indica che è presente la settima minore e la nona.<br>

<br><li> Un "11" indica che è presente la settima minore la nona e la undicesima.<br>

<br><li> Un "13" indica che è presente la settima minore la nona la undicesima e la tredicesima.<br>

<br><li> Un "add" indica che bisogna aggiungere una nota. Ad esempio "DOadd9" designa l'accordo di DO (DO-MI-SOL) più la nona (= DO-MI-SOL-RE). In questo caso l'aggiunta della nona non implica l'aggiunta della settima minore. Lo stesso discorso vale per "add11" "add13" etc.<br>

<br><li> Un "sus" indica che l'accordo è sospeso e di solito al posto della terza vi è la 2a o la 4a. Ad esempio "DOsus4" indica l'accordo di DO privato della terza (MI) con in più la nota indicata, in questo caso a un intervallo di quarta da DO troviamo il FA (= DO-FA-SOL).<br>

<br><li> Un "b" indica che tale intervallo va abbassato di mezzo tono. Ad esempio un "DO b9" indica DO-MI-SOL-RE bemolle. Nota che in questo caso "DO b9" = "DOaddb9" e per specificare una eventuale settima minore si deve ricorrere alla notazione "DO7/b9". <br>

<br><li> Un "#" indica che tale intervallo va alzato di mezzo tono. Ad esempio un "DO #11" indica DO-MI-SOL-FA diesis. Nota che anche in questo caso "DO #11" = "DOadd#11" e per specificare una eventuale settima minore e nona inclusa nell'accordo si deve ricorrere alla notazione "DO9/#11". <br>

<br><li> Un "+" indica che la quinta è eccedente. Ad esempio DO+ non significa DO maggore ma DO5+ o DO#5  (= DO-MI-SOL#), nota che la quinta giusta (qui SOL) viene omessa.<br>

<br><li>Un "-5" o "5-" o "b5" indica che la quinta è diminuita. Ad esempio DO (-5) significa DO-MI-SOLb. Nota ancora che la quinta giusta (qui SOL) viene omessa.<br>

<br><li>Un "°" indica un accordo di 7a diminuita. 
Ad es. DO° = DO-MIb-SOLb-SIbb(La), ma a volte viene inteso anche come DO-MIb-SOLb.<br>
Qui useremo "°"  per indicare l'accordo diminuito con settima diminuita e "dim" per indicare la triade diminuita.
