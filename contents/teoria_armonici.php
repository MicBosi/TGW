<font size=5>N</font>ella sezione precedente abbiamo visto quali siano gli intervalli consonanti e quali siano quelli dissonanti.<br>
Il concetto di dissonanza e consonanza è stato per molto tempo basato solamente sulla pratica dei grandi compositori antichi, sulla tradizione e sull'intuizione musicale dei singoli artisti.<br>
Oggi al concetto di consonanza e dissonanza è stata data una spiegazione fisica, basata sullo studio delle forme d'onda che i vari strumenti generano, naturalmente questo non c'entra niente con il fatto che uno pensi che certi intervalli piacciano più altri, la teoria fisica degli armonici si limita solamente a mettere in evidenza la natura dei suoni e le relazioni che hanno con altri suoni.<br>
Il risultato che queste relazioni hanno sulla psiche umana potrebbe anche essere diversa da individuo a individuo, ma in realtà, a parte un certo margine, questo non pare essere il caso e su questo presupposto si basano le più recenti tecniche di <i>musicoterapia</i>.<br>

<hr width=50%>

<font size=5>L</font>o studio delle forme d'onda dei vari strumenti ha rivelato che ogni genere di suono generato da uno strumento musicale è costituito da una serie ben determinata di suoni più semplici con frequenze diverse che suonano in simultanea: la cosiddetta <i>serie degli armonici</i>.<br>
Questi suoni sono molto numerosi e diventano sempre più acuti e deboli man mano che si procede lungo la serie, i primi tra essi sono: 8va, 5a, 8va, 3a maggiore, 5a, 7a minore.<br>
Essi spiegano perché due suoni che distano questi precisi intervalli risultino "omogenei" al nostro orecchio: essi sono proprio i suoni che chiamiamo <i>consonanti</i>.<br>
Prendiamo ad esempio la nota Do, i primi sei armonici che incontreremo sono: Do, Sol, Do, Mi, Sol, Sib, disposti in diverse ottave.<br>

