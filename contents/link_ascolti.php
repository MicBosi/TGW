<tr>
   <td width=50%>
<li><a href="http://www.satriani.com/">Joe Satriani</a>
   </td>
   <td>
Joe Satriani è stato il maestro di chitarristi del calibro di Steve Vai e Kirk Hammet, la sua musica è estremamente ricercata e innovativa.
</tr>
<tr>
   <td width=50%>
<li><a href="http://www.vai.com">Steve Vai</a>
   </td>
   <td>
Steve Vai come il suo maestro (Joe Satriani) ama la musica ricercata e le sonorità inusuali ma è molto più affascinato da soluzioni orientaleggianti e iperespressive.
</tr>
<tr>
   <td width=50%>
<li><a href="http://www.yngwie.org/">Malmsteen</a>
   </td>
   <td>
Yngwie è il capostipite di un modo di suonare la chitarra che è tutt'ora l'influenza principale di gran parte dei più grandi chitarristi. Da lui nasce il Metal Neoclassico con tutte le sue più o meno originali varianti.
</tr>
<tr>
   <td width=50%>
<li><a href="http://www.johnpetrucci.com">Dream Theater</a>
   </td>
   <td>
Questa band rappresenta il punto di riferimento per moltissimi giovani chitarristi e John Petrucci è probabilmente il guitar hero  più ammirato del momento. Questo gruppo ha avuto ed ha tutt'ora il merito di offrire un tipo di musica del tutto originale grazie a soluzioni sonore insolite come tempi dispari fortemente sincopati, un disinvolto uso di dissonanze e di scale alternative. Il link si riferisce al sito ufficiale di Petrucci da cui puoi scaricare alcuni interessanti MP3.
</tr>
<tr>
   <td width=50%>
<li><a href="http://www.symphonyx.com/">Symphony X</a>
   </td>
   <td>
Definire i Symphony X è molto difficile, a volte vengono accostati a gruppi metal-sinfonici altre volte a nomi quali i Dream Theater, di certo l'intento di questa band è tutto proteso a scrivere musica di qualità superiore rispetto anche ai soliti nomi famosi. Gli arrangiamenti, la musicalità e una sezione chitarristica che fa capo al formidabile Michael Romeo sono i punti forti della band.
<br>Nel sito si trovano anche moltissimi midi, e tablature alcuni dei quali realizzati dallo stesso Michael Romeo.
</tr>
<tr>
   <td width=50%>
<li><a href="http://www.snakepit.org/">Slash</a>
   </td>
   <td>
Questo sito è pieno zeppo di materiale riguardante Slash, troverai tablature, MP3, video ed altro ancora.
  </td>
</tr>
<tr>
   <td width=50%>
<li><a href="http://www.blind-guardian.com">Blind Guardian</a>
   </td>
   <td>
I Blind Guardian si sono guadagnati un posto d'onore nella scena metal odierna, l'armonia, la melodia e le sonorità rievocate da questo gruppo non sono paragonabili a quelle di nessun'altro come pure la loro voce, ascoltare per credere.
</tr>
<tr>
   <td width=50%>
<li><a href="http://www.helloween.org">Helloween</a>
   </td>
   <td>
Un altro gruppo che ha fatto e sta facendo la storia del metal, la loro musica è estremamente melodica, intrigante, tecnica al punto giusto e sprigiona energia da tutti i pori.
</tr>
