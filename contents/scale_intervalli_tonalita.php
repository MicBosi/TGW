<b><font size=5>C</font></b>ome abbiamo detto le scale sono essenzialmente insiemi di note (tipicamente 7) e si differenziano tra loro per 2 cose, la tonalità e le distanze in semitoni tra le varie note che la compongono.<br>
La tonalità indica la nota da cui cominciamo a "contare" le rimanenti note, le distanze tra le note indicano lo scheletro, potremmo dire l'essenza, della scala stessa.<br>
Ad esempio riprendendo in esame la Scala Maggiore di Do abbiamo che i suoi suoni sono ordinatamente: Do Re Mi Fa Sol La Si.<br> Come vediamo le note che la compongono partono da Do e per questo si chiama scala di Do: <u>questa è la sua tonalità</u>.<br>
Inoltre la sua struttura è <u>T+T+S+T+T+T+S</u>, il che significa che tra la prima nota e la seconda passa 1 tono, tra la seconda e la terza passa 1 tono, tra la terza e la quarta passa <sup>1</sup>/<sub>2</sub> tono, tra la quarta e la quinta nota passa 1 tono, tra la quinta e la sesta passa 1 tono, tra la sesta e la settima passa 1 tono, tra la settima e l'ottava (che sarebbe la prima ripetuta) passa <sup>1</sup>/<sub>2</sub> tono.<br>
Dicendo la stessa cosa con un altro schemetto risulata: <b>Do </b>-T-<b> Re </b>-T-<b> Mi </b>-S-<b> Fa </b>-T-<b> Sol </b>-T-<b> La </b>-T-<b> Si </b>-S-<b> Do </b>.<br>
In pratica tra le note intercorre un tono, eccetto che tra Mi-Fa e Si-Do che distano un semitono: questa è la successione di toni e semitoni che caratterizza univocamente la <i>Scala Maggiore</i>.<br>
Per rendere più completa la lettura degli schemi delle scale di solito non si usa la notazione T+T+S+T+T+T+S, ma si usa ad esempio sempre per la Scala Maggiore la notazione: 1 2 3 4 5 6 7maj.<br>
Ovvero ad ogni nota viene assegnato un numero da 1 a 7 partendo dalla nota che conferisce la tonalità alla scala e accanto ad ogni numero viene messo un simbolo tra #, m, dim, maj, che determina la caratteristica precisa di quella nota: più avanti descriveremo in dettaglio il significato di questi simboli. Questa notazione diviene particolarmente agevole soprattutto per il confronto tra le scale in quanto mantiene un'informazione specifica per ogni nota della scala, questo pero' lega la notazione stessa a un numero di note pari a 7 e perde gran parte del suo significato per un numero di note diverso.<br>
Ricapitolando tutto abbiamo che il nome di una scala è: Scala <i>"tipo scala"</i> di <i>"tonalità"</i>, in cui <i>"tipo scala"</i> è determinato da una precisa consecuzione di toni e semitoni e <i>"tonalità"</i> non è altro che la nota di partenza da cui cominciamo a contare i nostri toni e semitoni per trovare le altre note.<br>
E' facile intuire che uno stesso tipo di scala è costruibile su tutte le tonalità, cioè a partire da tutte e 12 le note, basterà rispettare la consecuzione di toni e semitoni che caratterizzano il tipo di scala. Per cui non esisterà solo la Scala Maggiore di Do ma anche, e con uguale dignità, la Scala Maggiore di Re, di Mi, di Fa, ed anche di Fa# o di Mi<i>b</i>, cosi' per tutte e 12 le note fondamentali.<br>
Ad esempio la scala maggiore di Sol risulta essere: Sol La Si Do Re Mi Fa#.<br>
Nota che la consecuzione di toni e semitoni risulta ancora essere T + T + S + T + T + T + S: per ottenere questo pero' è stato necessario prendere "le note giuste". Se ad esempio al posto del Fa# avessimo preso il Fa (detto Fa naturale per distinguerlo dal Fa# e dal Fab), la consecuzione di toni e semitoni sarebbe stata: T + T + S + T + T + S + T, il risultato sarebbe stato una scala di Sol di tipo diverso dalla Scala Maggiore, ed avrebbe avuto una sonorità molto diversa.<br>
Inoltre è importante notare che la nota Fa# come si è visto in <i><a href="toni.html">Suoni, Semitoni, Toni </a></i> puo' essere chiamata anche Sol<i>b</i>. In pratica esse corrispondono allo stesso suono ma per motivi che tornano utili nell'<i>armonia</i> si prendono tutte note che hanno nome diverso rispettando la "naturale" consecuzione Do, Re Mi Fa Sol La Si, modificandone a seconda delle esigenze la nomenclatura usando i # (diesis) e b (bemolle), per alzare o abbassare le 7 note naturali, che, ricordo ancora, sono Do Re Mi Fa Sol La Si.<br> Questo è il motivo per cui nella scala di Sol Maggiore (Sol La Si Do Re Mi Fa#) non possiamo prendere Sol<i>b</i> al posto di Fa#, in questo caso salteremmo una nota (il Fa#) e ne avremmo una doppia il Solb e il Sol, il tutto si complica perché il Sol è anche "il tenutario" della tonalità.<br>
Sempre per questi motivi la Scala Maggiore è detta <i>diatonica</i>, ovvero i nomi delle sue note sono tutti diversi.<br>
Oltre a tutti questi motivi si aggiunge anche il problema del conteggio degli intervalli.<br>
Ad esempio tra la nota Do e la nota Mi si dice che passa un <i>intervallo di terza</i>, perché contando le note comprese tra le due citate, esse comprese, abbiamo un totale dei tre note: Do, Re e Mi.<br> Tra La e Do intercorre sempre un intervallo di terza: La, Si e Do. Allo stesso modo tra Fa e Si intercorre un intervallo di quarta: Fa, Sol, La e Si.<br>
Viceversa se diciamo di voler salire di un intervallo di quarta (quarta ascendente) da Do, nella Scala Maggiore di Do ci ritroveremo in Fa (Do->Re->Mi->Fa), mentre nella scala Maggiore di Sol ci troveremo in Fa# (Do->Re->Mi->Fa#). Se il conteggio dell' intervallo procede invece da una nota più acuta ad una più grave diremo che l'intervallo è discendente: ad es. compiendo un salto di quarta discendente da Do, nella Scala Maggiore, ci troveremo in Sol (Do->Si->La->Sol).<br>
Nota che questo conteggio non indica la distanza in toni o semitoni tra due note, ma solo quante note sono coinvolte nell'intervallo, ad esempio l'intervallo di terza Do->Re->Mi consta di 2 toni, mentre l'intervallo di terza Re->Mi->Fa consta di 1 tono e <sup>1</sup>/<sub>2</sub>. Nonostante questo il calcolo di un intervallo, inteso come numero di note coinvolte in esso, è di centrale importanza nello studio dell'armonia.<br>
