<br><b><font size=5>G</font></b>li intervalli detti <font !size=+1><u>giusti</u></font> possono essere <font !size=+1><u>aumentati</u></font> o <font !size=+1><u>diminuiti</u></font>, rispettivamente aggiungendo o togliendo ad esso un semitono: essi non vengono mai detti <font !size=+1><u>maggiori</u></font> o <font !size=+1><u>minori</u></font>.<br>
Gli intervalli detti <font !size=+1><u>maggiori</u></font> possono essere <font !size=+1><u>aumentati</u></font>, <font !size=+1><u>minori</u></font> o <font !size=+1><u>diminuiti</u></font>, rispettivamente aggiungendo ad esso un semitono, o togliendo ad esso uno o due semitoni: essi non vengono mai detti <font !size=+1><u>giusti</u></font>.<br>
L' intervallo tra due note con uno stesso nome, ad esempio Do, ma intonazione diversa, cioè uno più acuto e uno più grave, si dice intervallo di <i>ottava giusta</i>.<br><br>
<b><font size=5>O</font></b>ra sveliamo il significato dei simboli #, m, dim, maj, utili nella schematizzazione a numeri di una scala.<br>
Abbiamo detto che la formula 1, 2, 3, 4, 5, 6, 7maj, rappresenta lo schema della Scala Maggiore.
Intanto partiamo col dire che i numeri che accanto non hanno alcun simbolo caratterizzano un intervallo maggiore o uno giusto: ad esempio un "2" da solo significa "seconda maggiore", un "5" da solo significa "quinta giusta".<br>
Solo l'intervallo di <i>settima maggiore</i> viene segnalato con un "7maj" o "j7" per distinguerlo univocamente da quello di <i>settima minore</i> che nelle siglature degli accordi viene scritto proprio con un "7" da solo.<br>
Il simbolo "#" significa che l'intervallo è aumentato.<br>
Il simbolo "m" significa che l'intervallo è minore.<br>
Il simbolo "dim" significa che l'intervallo è diminuito.<br>
Il simbolo "b" viene a volte indistintamente usato al posto di "dim" o "m" e significa che l'intervallo è minore (con 2,3,6) o diminuito (con 4,5).<br>
Poichè ad ogni nota di una scala puo' essere attribuito un numero progressivo, esse possono anche essere chiamate <i>gradi</i>. Ad esempio il I° grado della Scala Maggiore di Do è Do, il II° grado è Re, e cosi' via: tipicamente i gradi vengono contrassegnati da numeri romani.<br>
Ad esempio la Scala Frigia ha la seguente formula: 1, 2m, 3m, 4, 5, 6m, 7m, il che significa che ha il II, III, VI e VII grado abbassati di un semitono rispetto alla Scala Maggiore.<br>
In generale una scala è detta di modo maggiore se tra il suo I° e III° grado corre una terza maggiore, viceversa èdi modo minore se tra il suo I° e III° grado corre una terza minore.<br>
La differenza tra la sonorità di una scala maggiore e una minore è che quelle minori hanno solitamente un suono più "triste" di quelle maggiori, che invece hanno un carattere più "gioioso".
