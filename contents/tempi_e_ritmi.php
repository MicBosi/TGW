<br><b><font size=5>I</font></b>n quei tempi particolarmente complessi la costruzione delle singole parti della battuta diventa suscettibile di diverse combinazioni, per cui la sottostante frase musicale farà la differenza.<br><br>
<b><font size=5>S</font></b>i ha un <i>contrattempo</i> quando incontriamo una pausa che cade su un tempo forte della misura e la frase musicale ricomincia sul tempo debole.<br><br>


<b><font size=5>S</font></b>i ha una <i>sincope</i> quando una nota cade su un accento debole della battuta per finire su di uno forte creando effetti di anticipazione e posticipazione dell'accento in questione.<br><br>

