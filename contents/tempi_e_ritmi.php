<font size=5>Figure ritmiche particolari</font>


<br><br>
<b><font size=5>L</font></b>a TERZINA.<br>
Si ha una terzina quando due note regolari (ad esempio di un tempo 4/4) di uguale valore vengono sostituite da tre note uguali fra loro e il cui valore complessivo è uguale alla somma delle due sostituite. Se le due note sostituite sono due crome, la terzina sarà raffigurata come un gruppo di 3 crome unite da un archetto e un "3". Naturalmente le "nuove" tre crome valgono meno delle due "vecchie" , cioè vanno eseguite più velocemente per non andare fuori tempo.<br>

<br><b><font size=5>L</font></b>a SESTINA.<br>
Si ha una sestina quando quattro note regolari (ad esempio di un tempo in 4/4) di uguale valore vengono sostituite da sei note uguali fra loro e il cui valore complessivo è uguale alla somma delle quattro sostituite. Se le quattro note sostituite sono quattro crome, la sestina sarà raffigurata come un gruppo di 6 crome unite da un archetto e un "6". Anche qui le "nuove" sei crome valgono meno delle quattro "vecchie", cioè vanno eseguite più velocemente per non andare fuori tempo. <br>
 
<br>Esempio di terzine e sestine.<br><img src="fragments/terzine e sestine.gif" Alt="Terzine e Sestine"><br><br>

<b><font size=5>L</font></b>a DUINA.<br>
Si ha la duina quando tre note regolari (ad esempio di un tempo in 12/8) di uguale valore vengono sostituite da due note di valore uguale fra loro e il cui valore complessivo è uguale alla somma delle tre sostituite. Se le tre note sostituite erano tre crome , la duina sarà raffigurata come un gruppo di 2 crome unite da un archetto e un "2". Naturalmente le "nuove" due crome valgono più delle 2 "vecchie" , cioè vanno eseguite più lentamente per non andare fuori tempo. <br>

<br>Esempio di duine.<br><img src="fragments/duine.gif" Alt="Duina"><br><br>

<br><b><font size=5>N</font></b>aturalmente sia le terzine che le sestine che le duine possono essere formate da gruppi di semicrome, biscrome etc.
