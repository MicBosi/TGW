<b><font size=5>I</font></b>l numero in frazione che vedi nell'immagine sopra indica il tempo che misurano le singole battute del pezzo che segue.<br>

I tempi designano una particolare consecuzione di accenti all'interno della battuta.<br>
Gli accenti sono cio' che caratterizzano un certo tempo. Essi possono essere forti o deboli.<br>
Quando noi teniamo il tempo col piede mentre ascoltiamo una canzone "battiamo gli accenti forti". <br>
Anche quando suoniamo un pezzo di solito "battiamo" più forte sulle le note o gli accordi che cadono sui tempi forti, questo ci aiuta a esprimere il senso ritmico.<br>
Una battuta quindi non è altro che un insieme di accenti forti e deboli che si susseguono in un certo ordine.<br>
La frazione che abbiamo visto serve proprio a questo, a specificare numero e durata degli accenti presenti in una battuta.<br>
In particolare il numero che sta sopra (numeratore) indica quanti accenti abbiamo nella nostra battuta, il numero che sta sotto (denominatore) indica la durata dei singoli accenti.<br>
La somma delle durate, delle note e delle pause, presenti in una battuta deve rientrare nel tempo specificato per la frazione.<br>
Ad esempio, nella figura sopra vediamo in una battuta in 4/4 rientrano quattro semiminime (durata 1/4).<br>

