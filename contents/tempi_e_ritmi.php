<b><font size=5>S</font></b>e uniamo più tempi semplici in un unico tempo otteniamo un <i>tempo composto</i>.
Ad esempio 6/8 , 9/8 , 12/8, 5/4, 7/8 11/8 sono tempi composti.<br>
La sequenza di battiti di questi tempi è la somma dei battiti dei tempi semplici che li compongono.<br>
Quando il numero dei tempi che compongono una battuta non è divisibile per due o per tre viene detto <i>tempo asimmetrico</i>.<br>
