<font size=5>L</font>e quadriadi sono del tutto analoghe alle triadi, fatta eccezione per il fatto che al posto di avere tre note sovrapposte a distanza di terza qui abbiamo <i>quattro note disposte a distanza di terza</i> e per questo essa è detta anche <i>accordo di settima</i> poichè tra la fondamentale e la quarta nota passa un intervallo di settima.<br>
Analogamente alla triade i suoi quattro suoni sono detti:<br><br>
<li>il primo <i>fondamentale</i>
<li>il secondo <i>terza</i>
<li>il terzo <i>quinta</i>
<li>il quarto <i>settima</i>
<br><br>
<font size=5>G</font>li accordi di settima sono divisi in <i>sette specie</i>, diversificate tra loro dai diversi tipi di intervalli di terza, quinta e settima da cui sono formati.
<br>Nota che gli accordi di settima costruibili sfruttando tutte le combinazioni di 3<sup>e</sup>, 5<sup>e</sup> e 7<sup>e</sup> possibili sono ben più di sette!<br><br>
<ol type=I>
<li>Accordo di settima di I specie o settima di dominante:<br>
<br>Questo accordo si costruisce sul V grado della Scala Maggiore e presenta la struttura:<br>
<i>3<sup>a</sup> maggiore + 5<sup>a</sup> giusta + 7<sup>a</sup> minore</i>.
<br>Si indica con un "7" dopo il nome della fondamentale:
Ad es. Sol7 = Sol Si Re Fa.<br>

<br><li>Accordo di settima di II specie:<br>
<br>Questo accordo si costruisce sul II, III, VI grado della Scala Maggiore e presenta la struttura:<br>
<i>3<sup>a</sup> minore + 5<sup>a</sup> giusta + 7<sup>a</sup> minore</i>.
<br>Si indica con un "m7" dopo il nome della fondamentale:<br>
Ad es. Rem7 = Re Fa La Do.<br>

<br><li>Accordo di settima di III specie:<br>
<br>Questo accordo si costruisce sul VII grado della Scala Maggiore e presenta la struttura:<br>
<i>3<sup>a</sup> minore + 5<sup>a</sup> diminuita + 7<sup>a</sup> minore</i>.
<br>Si indica con un "dimm7" o "°m7" dopo il nome della fondamentale:<br>
Ad es. Sidimm7 o Si°m7 = Si Re Fa La.<br>

<br><li>Accordo di settima di IV specie:<br>
<br>Questo accordo si costruisce sul I e IV grado della Scala Maggiore e presenta la struttura:<br>
<i>3<sup>a</sup> maggiore + 5<sup>a</sup> giusta + 7<sup>a</sup> maggiore</i>.
<br>Si indica con un "maj7" dopo il nome della fondamentale:<br>
Ad es. Domaj7 = Do Mi Sol Si.<br>

<br><li>Accordo di settima di V specie o settima diminuita:<br>
<br>Questo accordo si costruisce sul VII grado della scala Minore Armonica e presenta la struttura:<br>
<i>3<sup>a</sup> minore + 5<sup>a</sup> diminuita + 7<sup>a</sup> diminuita</i>.
<br>Si indica con un "dim7" o "7°" dopo il nome della fondamentale:<br>
Ad es. Sol#7° o Sol#dim7 = Sol# Si Re Fa.<br>


<br><li>Accordo di settima di VI specie:<br>
<br>Questo accordo si costruisce sul I grado della scala Minore Armonica e presenta la struttura:<br>
<i>3<sup>a</sup> minore + 5<sup>a</sup> giusta + 7<sup>a</sup> maggiore</i>.
<br>Si indica con un "mmaj7" dopo il nome della fondamentale:<br>
Ad es. Lammaj7 = La Do Mi Sol#<br>


<br><li>Accordo di settima di VII specie:<br>
<br>Questo accordo si costruisce sul III grado della scala Minore Armonica e presenta la struttura:<br>
<i>3<sup>a</sup> maggiore + 5<sup>a</sup> aumentata + 7<sup>a</sup> maggiore</i>.
<br>Si indica con un "maj7#5" dopo il nome della fondamentale:<br>
Ad es. Domaj7#5 = Do Mi Sol# Si.<br>
</ol>

<font size=5>I</font>l discorso delle armonizzazioni a quadriadi continuerà in una prossima sezione dedicata alle risoluzioni degli accordi, ovvero quelle "leggi" o meglio, princìpi, che indicano quali accordi "suonano bene" se suonati uno di seguito all'altro, a rileggerci quindi...
