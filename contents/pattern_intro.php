<font size=5>I</font> pattern sono delle sequenze predefinite di note, delle idee "standard" che spesso i musicisti inseriscono, più o meno variati, nelle loro composizioni.
<br>Essi possono essere suonati su una corda, su un gruppo di corde, su un BOX, insomma ovunque e come vogliamo.
<br>Conoscere il maggior numero di pattern vuol dire conoscere tante idee base su cui poter lavorare, fare variazioni, prendere spunti, in pratica vuol dire avere un vocabolario di idee più ricco.
<br>Esercitarsi su questi pattern è spesso un modo per imparare un BOX o una nuova scala e per vedere quanto controllo abbiamo su di essa.
<br>I pattern sviluppano l'agilità e l'indipendenza delle tue dita, ti permettono di vedere quali sequenze o combinazioni di movimenti ti risultano più difficili e inoltre ti danno un sommario metodo di misura delle tue capacità tecniche.
<br>La maggior parte dei pattern infatti non sono facili e se riuscirai a suonare in maniera fluida e sicura tanti tipi di pattern su diversi BOX, in diverse scale, saprai di avere raggiunto un discreto livello tecnico.
