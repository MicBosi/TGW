<font size=5>L</font>'insieme di tre note diverse ordinate per terze si dice triade e in particolare:<br><br>
<i>
<li> Si dice triade maggiore la sovrapposizione di una terza maggiore e una minore.<br>
</i></b>Ad esempio la triade Do Mi Sol: <b>Do</b>-terza maggiore-<b>Mi</b>-terza minore-<b>Sol</b>.<b><i><br>
<img src="fragments/triad maj.gif" Alt="Esempio triade maggiore"><br><br>

<li> Si dice triade minore la sovrapposizione di una terza minore e una maggiore.<br>
</i></b>Ad esempio la triade Mi Sol Si: <b>Mi</b>-terza minore-<b>Sol</b>-terza maggiore-<b>Si</b>.<b><i><br>
<img src="fragments/triad min.gif" Alt="Esempio triade minore"><br><br>

<li> Si dice triade diminuita la sovrapposizione di due terze minori.<br>
</i></b>Ad esempio la triade Si Re Fa: <b>Si</b>-terza minore-<b>Re</b>-terza minore-<b>Fa</b>.<b><i><br>
<img src="fragments/triad dim.gif" Alt="Esempio triade diminuita"><br><br>

<li> Si dice triade aumentata la sovrapposizione di due terze maggiori.<br>
</i></b>Ad esempio la triade Do Mi Sol#: <b>Do</b>-terza maggiore-<b>Mi</b>-terza maggiore-<b>Sol#</b>.
<i><br>
