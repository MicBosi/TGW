</i>
<font size=5>P</font>er quello che abbiamo detto alla fine del paragrafo precedente la triade è un accordo.
Il primo suono della triade è detto <i>fondamentale</i>, in quanto è quello che genera la triade e ne denota tonalità e nome. Ad esempio la triade maggiore di Do è Do Mi Sol.<br>
Il secondo e il terzo suono della triade prendono il loro nome dall'intervallo che formano rispetto la fondamentale e quindi sono detti rispettivamente <i>terza</i> e <i>quinta</i>.<br>
<br>
<font size=5>S</font>e prendiamo gli stessi suoni e li ordiniamo in ordine diverso essi formano comunque una triade, ad esempio qua sotto sono mostrati diversi modi di ordinare i suoni della triade maggiore di Do:<br>
<img src="fragments/triad rivolti.gif" Alt="Esempio triade diminuita"><br><br>
<font size=5>Q</font>uando una triade ha la sua fondamentale al basso si dice che si trova allo <i>stato fondamentale</i>: caso 1 della figura sopra.<br>
<br>
<font size=5>Q</font>uando una triade ha al basso una nota diversa dalla sua fondamentale si dice che si trova allo <i>stato di rivolto</i>: caso 2, 3 della figura sopra.
In particolare una triade è allo stato di:<br>
<li><i>secondo rivolto</i> se al basso abbiamo la sua terza.<br>
<li><i>terzo rivolto</i> se al basso abbiamo la sua quinta.<br>

<br>
<font size=5>L</font>e triadi possono anche essere classificate in base alla nota più acuta e in particolare abbiamo che la triade si dice:<br>
<li><i>in prima posizione</i> se all'acuto abbiamo la sua nota fondamentale.<br>
<li><i>in seconda posizione</i> se all'acuto abbiamo la sua terza.<br>
<li><i>in terza posizione</i> se all'acuto abbiamo la sua quinta.<br>
<br>
<font size=5>N</font>ota che fin qui abbiamo presupposto che i tre suoni della triade siano tutti presenti e ordinati senza salti. Le pagine che seguono trattano i casi in cui cio' non accade.
