<font size=5>Q</font>uesta sezione intende presentare per ogni scala diverse tipologie di diteggiature e per ognuna di esse, metterne in luce gli usi più o meno consoni.<br>
Allo stato attuale GuitarWizard presenta le diteggiature classiche a BOX, ma nel breve futuro verranno proposte molte altre tipologie di diteggiature, più "orizzontali" e se possibile cercheremo di compararle tra loro usando come parametri di valutazione la loro efficacia nella risoluzione dei diversi problemi "solistici".<br><br>

<font size=5>P</font>er poter fare degli assoli con la nostra chitarra e ancora di più se vogliamo crearne dei nostri è necessario essere in grado di "performare" i vari tipi di scale sulla nostra tastiera.<br> 
Si, però visto che una stessa scala (data la ridondanza delle note sulla tastiera della nostra chitarra) può cominciare su diverse posizioni, ogni volta dobbiamo scegliere quale posizione privilegiare e ciò rende ulteriormente difficoltoso visualizzare sulla tastiera una qualsiasi scala. Come se non bastasse a seconda dell'andamento del fraseggio e delle varie tecniche che vogliamo coinvolgere in esso e del suono che vogliamo raggiungere, ci troviamo ancora una volta a scegliere tra una più o meno vasta rosa di <i>diteggiature</i> di diversa efficacia.<br>

Il primo tipo di scale che un chitarrista impara sono quelle a posizioni fisse: scegliamo le posizioni delle note sulla tastiera in modo tale da muovere il meno possibile la mano.<br>
In pratica si prendono in considerazione 4 o 5 tasti alla volta su tutte e sei le corde e visualizziamo i tasti in cui ricadono  le note della scala in questione.<br>

Prendiamo ad esempio lo schema della tastiera con le note della scala maggiore, consideriamo un quadrato (box) che ci permetta di fare una scala senza spostare la mano:<br> <br> 

