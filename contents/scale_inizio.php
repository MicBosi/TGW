<font size=5>Q</font>uesto tipo di schematizzazione viene detta "BOX" o "SHAPE" o "PATTERN", in questo sito useremo il termine BOX perché pare essere il più auto esplicativo.<br>
Esso indica semplicemente come porre sulla tastiera le dita per ottenere una certa scala (i pallini chiari indicano le posizioni delle toniche).<br><br>

<font size=5>P</font>ossiamo continuare e trovare altri box , ad esempio<br><br>

<Img src="saccordi\tastiera-box2.gif">
<br><br>
e lo schema che ne traiamo è questo ( BOX II )<br>
<Img src="saccordi\smallmaj2.gif">
<br><br>
<font size=5>C</font>ontinuando così fino a coprire tutta la tastiera possiamo individuare cinque BOX uno "attaccato" all' altro.<br>
Tale metodo è estremamente efficace, infatti tali BOX si prestano ad essere ricordati facilmente e una volta imparati tutti e i 5 BOX si puo' saltare sue giu' per la tastiera quanto si vuole.
<br>Non solo. Se noi spostiamo avanti o indietro lungo i tasti lo schema del nostro box, possiamo ottenere tutte le tonalità di quella scala, senza doverci preoccupare di ricalcolare tutte le distanze e i box.<br>
Se noi ad esempio spostassimo il BOX I in modo tale che la sua tonica (2° pallino, quello bianco, in basso) coincida con la nota SOL , otterremo il BOX della  scala maggiore di SOL (vedi immagine sotto).
<br>Concatenando mentalmente a quello tutti gli atri quattro box, siamo in grado di coprire di nuovo tutta la tastiera nella tonalità da noi scelta.<br>
<br>
BOX I in tonalità di SOL.<br>
<img src="saccordi\boxsolmaj.gif" alt="BOX di LA MAGGIORE">
<br><br>

<font size=5>Q</font>uindi i BOX non cambiano se cambiamo tonalità ma rimaniamo nello stesso tipo di scala, ad esempio i BOX visti sopra si riferiscono alla Scala Maggiore. Questo perché i BOX rappresentano solo le distanze e posizioni che fra loro hanno le note di un certo tipo di scala.<br>
Questo significa che i BOX della scala Minore Armonica avranno una "forma" diversa e cosi quelli della scala Minore Melodica etc...<br>

<br><font size=5>I</font>n seguito aggiungero' altri tipi di diteggiature delle scale e se possibile cercheremo di scoprire quali sono gli usi più consoni per ognuna di esse.<br>

<br><font size=5>Q</font>ua sotto c'è il tipo di schematizzazione che useremo noi, le note colorate in rosso o blu sono le note che formano il nostro Box. Le note in bianco e nero servono a vedere su quale punto della scala ci stiamo muovendo e a non perdere di vista le note che abbiamo intorno, inoltre il nostro BOX ci informa anche del grado di ogni singola nota, permettendoci di avere un controllo completo sulla scala.<br><br>


