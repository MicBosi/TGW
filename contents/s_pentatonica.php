          <br />
          La scala Pentatonica come vediamo presenta degli intervalli molto ampi che le derivano le
          ben note qualità di "incisività" e "robustezza".<br />
          Queste qualità le derivano anche dal fatto che i suoi suoni sono cinque sovrapposizioni
          di quinta giusta, che guarda caso è l'intervallo armonico per eccellenza. Prendendo ad
          esempio la nota DO, volendo prendere la nota che sta una 5<sup>a</sup> sopra, otterremo
          SOL, poi prendiamo la 5<sup>a</sup> del SOL, cioè RE, e cosi' per LA e MI.<br />
          Essa è molto importante per un chitarrista in quanto i sui BOX sono molto facili da
          imparare, la sua struttura è ideale per cominciare ad allenare le dita a correre sulla
          tastiera e in particolare è utile per la tecnica del mignolo.<br />
          Come se non bastasse essa ha una forte capacità didattica per il nostro orecchio musicale
          in quanto ci abitua a "sentire" più facilmente l'attrazione che esercitano le note
          principali.<br />
