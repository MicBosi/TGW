          <br/>
          Questa nuova scala è chiamata <em>Pentatonica Minore</em>. "Pentatonica" si riferisce al
          fatto che possiede "soltanto" 5 suoni, "minore" perchè tra La e Do passa una terza
          minore.<br/>
          <br/>
          Anche su questa scala può essere costruita la <em>relativa maggiore</em> che si pone 1
          tono e 1/2 sopra, in questo caso sopra 1 tono e 1/2 sopra il La troviamo il Do. Quindi
          con gli stessi suoni cominciando a "contare" da Do otteniamo la <em>Pentatonica
          Maggiore</em> di Do che può essere pensata come il prodotto della privazione del
          <em>quarto</em> e <em>settimo</em> grado della scala maggiore:<br/>
