<br/>
Quando sto suonando la Pentatonica, come faccio a sapere se sto usando la Pentatonica
Maggiore o quella Minore? Ad esempio come faccio a sapere se sto in Do maggiore o La
minore, visto che i tasti che devo usare sono gli stessi?<br/>
La prima risposta che mi viene è booh, lo dovresti sentire "a orecchio" !<br/>
La seconda è: se la nota che ricorre più spesso è la quinta (dominante) della tonica
minore allora la scala è minore, se la nota che ricorre più spesso è la quinta della
tonica maggiore allora la scala è maggiore.<br/>
Se per esempio stiamo suonando sulla Pentatonica minore di LA , la nota che ricorre più
spesso sarà il MI. Se per esempio stiamo suonando sulla Pentatonica maggiore di DO , la
nota che ricorre più spesso sarà il SOL.<br/>
Questo perché sperimentalmente si vede che in un brano, la nota che ricorre più spesso è
la dominante della scala, in graduatoria dopo di lei probabilmente ci sarà la stessa
tonica, quindi occhio anche a lei.<br/>
Naturalmente il metodo più semplice è quello di trovare un finale per la nostra melodia e
verificare quale sia la nota conclusiva. Questo però ci dice solo con quale tonalità
finisce e non sappiamo niente di come si sia sviluppata lungo tutto il suo svolgimento.
Inoltre per il risultato finale molto dipende dal giro di accordi e di basso che c'è
sotto il nostro ipotetico riff o assolo. Questo a grandi linee...
