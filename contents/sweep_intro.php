          </font> n questa sezione introdurremo lo Sweep Picking applicato alle scale e agli arpeggi. In particolare ci concentreremo sugli arpeggi che coinvolgono 3 corde. Questi tipi di Sweep sono ideali per comincire a fare un po' di pratica e prendere confidenza con questa tecnica.
          <br> Ma cosa è lo Sweep Picking? Letteralmente potremmo tradurlo come "plettratta a spazzata", nome alquanto curioso ma che rende bene l'idea su cui si basa questa tecnica. L'obbiettivo dello Sweep Picking, detto anche Speed Picking dagli amanti del blasonatissimo Frank Gambale, è quello di minimizzare il numero di cambi di direzione delle plettrate (su-giu-su-giu-etc.) in modo da ottenere un movimento il più fluido e veloce possibie. Considera il seguente esempio:
<pre>
Con plettrata alternata (Alternate Picking)
n v  n v n  v n v  n v n  v n  v n
|--------------------------------7-8------
|---------------------------8-10----------
|--------------------7-9-10---------------
|-------------7-9-10----------------------
|------7-8-10-----------------------------
|-8-10------------------------------------
</pre> Non c'è dubbio che sia alquanto scomoda! Abbiamo in tutto 14 cambi di plettrata per 15 note!
          <br> Guarda invece l'esempio qua sotto
<pre>
Con Sweep Picking
v n  n v n  n v n  n v n  n v  n v
|--------------------------------7-8------
|---------------------------8-10----------
|--------------------7-9-10---------------
|-------------7-9-10----------------------
|------7-8-10-----------------------------
|-8-10------------------------------------
</pre> Abbiamo 10 cambi di plettrata contro i 14 visti prima, più di un quarto della fatica risparmiata, non è poco! e in più come dicevamo prima, anche la fluidità del fraseggio ne guadagna.
          <br> Ma nonostante le scale traggano vantaggio da questa tecnica, l'applicazione degli Sweep ha trovato il suo massimo successo nell'esecuzione di arpeggi ed è su questi che ci concentreremo. Considera quest'altro esempio:
<pre>
n  n n n n n v    v v v v v
|------------7-10p7------------------------
|----------7--------7----------------------
|--------7------------7--------------------
|------9----------------9------------------
|----9--------------------9----------------
|-10------------------------10-------------
4  3 3 1 1 1 4  1 1 1 3 3 4
</pre> Questo è un arpeggio "sweeppato" in LAm e racchiude in se tutti (o quasi) i concetti base dello Sweep, alcuni dei quali non abbiamo ancora preso in considerazione, come il Rolling, il legato (Hammer On/Pull Off) e il Muting.
          <br> Prima di tutto vediamo che i cambi di direzione del plettro sono ridotti all'osso, grazie anche all'uso del legato che vediamo sulla prima corda (10p7). Il legato (in certi casi anche anche lo Slide) è usato spessissimo insieme allo Sweep, perché consente di non dover plettrare una nota e quindi di mantenere uguale il verso della plettrata.
          <br> Lo stesso vale per le scale, infatti se ad esempio volessimo suonare un BOX della Pentatonica, ci accorgeremmo che per mantenere il verso della plettrata dovremmo usare per in continuazione il legato, come si vede dall'esempio qua sotto
<pre>
n   n   n   n   n   n     v   v   v   v   v
|---------------------5h8p5---------------------
|-----------------5h8-------8p5-----------------
|-------------5h7---------------7p5-------------
|---------5h7-----------------------7p5---------
|-----5h7-------------------------------7p5-----
|-5h8---------------------------------------8p5-
</pre> Non c'è che dire, grazie alla tecnica del legato abbiamo suonato 23 note con 1 cambio di direzione del plettro!
          <br> Da tutto cio' possiamo trarre delle considerazioni interessanti: se il numero di note che suoniamo consecutivamente sulla stessa corda è dispari allora la prima e l'ultima plettrata avranno lo stesso verso, quindi al prossimo cambio di corda avremmo il nostro Sweep, altrimenti avranno verso opposto e al cambio di corda non avremo lo Sweep.
          <br> Anche in quest'ultimo caso pero' possiamo ottenere lo Sweep al cambio di corda, semplicemente inserendo un numero di legature tali da ottenere un numero di plettrate dispari su quella corda. Vedi gli esempi seguenti:
          <br>
          <br>Guarda caso abbiamo un ipotetico fraseggio con un numero di note pari su tutte le corde. Che sfiga! non ci scappa neanche uno Sweep, 20 note per 19 cambi di direzione e 20 plettrate...
<pre>
n v  n v n  v n v n  v n v n  v n v  n v n  v
|--------------------------------------7-8-10-8--
|---------------------------------8-10-----------
|------------------------7-9-10-9----------------
|---------------7-9-10-9-------------------------
|------7-8-10-8----------------------------------
|-8-10-------------------------------------------
</pre>...ma a salvare la situazione ci pensa "Mister Legato" il super eroe preferito dagli sweeppatori, guarda qua
<pre>
n    n   v  n n   v  n n   v  n n    n   v  n
|--------------------------------------7h8-10-8--
|---------------------------------8h10-----------
|------------------------7h9-10-9----------------
|---------------7h9-10-9-------------------------
|------7h8-10-8----------------------------------
|-8h10-------------------------------------------
</pre> Sempre 20 note ma questa volta solo 8 cambi di direzione e 14 plettrate, ma possiamo fare anche meglio se siamo abbastanza ferrati sul legato, sorprenditi
<pre>
n    n        n        n        n    n
|--------------------------------------7h8h10p8--
|---------------------------------8h10-----------
|------------------------7h9h10p9----------------
|---------------7h9h10p9-------------------------
|------7h8h10p8----------------------------------
|-8h10-------------------------------------------
</pre> Questa volta abbiamo raggiunto il limite, sempre 20 note, 5 plettrate e ... 0 cambi di direzione!!! Arrivati a questo punto mi chiedo se non debba cambiare il titolo di questa lezione in "Sweep Legatoing"...
          <br>
          <br> Per questo motivo sono molto usate quelle ditegiature delle scale che hanno 3 note per corda, cio' permette di avere uno Sweep a ogni cambio corda, queste diteggiature non compaiono ancora nel sito ma saranno presentate nell'apposita sezione quanto prima, per ora ti presento solamente un esempio di Scala Maggiore di SOL eseguita con il suddetto approccio:
<pre>
n v n n v n n v n n v n n v n n v n v n v n v v n v v n v v n v v n v
|-------------------------------5-7-8-7-5-------------------------------
|-------------------------5-7-8-----------8-7-5-------------------------
|-------------------4-5-7-----------------------7-5-4-------------------
|-------------4-5-7-----------------------------------7-5-4-------------
|-------3-5-7-----------------------------------------------7-5-3-------
|-3-5-7-----------------------------------------------------------7-5-3-
</pre> La difficoltà di questo genere di scale è l'allargamento delle dita specialmente sui primi tasti, per il resto sono quasi identiche ai BOX che conosciamo già.
          <br>
          <br>Adesso è venuto il momento di passare decisamente a cose un po' meno usuali, torniamo agli Sweep su arpeggi e introduciamo il
          <b>Rolling</b>, forse l'aspetto più ostico dello Sweep. Riprendiamo lo schema visto sopra con particolare attenzione alle diteggiature
<pre>
n  n n n n n v    v v v v v
|------------7-10p7------------------------
|----------7--------7----------------------
|--------7------------7--------------------
|------9----------------9------------------
|----9--------------------9----------------
|-10------------------------10-------------
4  3 3 1 1 1 4  1 1 1 3 3 4
</pre> Il Rolling entra in campo quando suoniamo due o più note che si trovano su corde diverse ma sullo stesso tasto. Nell' esempio questo succede sulle corde 4-5 (Re-La)
<pre>
|------------7-10p7------------------------
|----------7--------7----------------------
|--------7------------7--------------------
|------<font color=yellow><b>9</font></b>----------------<font color=yellow><b>9</b></font>------------------
|----<font color=yellow><b>9</b></font>--------------------<font color=yellow><b>9</b></font>----------------
|-10------------------------10-------------
4  3 3 1 1 1 4  1 1 1 3 3 4
</pre> e 1-2-3 (Mi-Si-Sol)
<pre>
|------------<font color=yellow><b>7</b></font>-10p<font color=yellow><b>7</b></font>------------------------
|----------<font color=yellow><b>7</b></font>--------<font color=yellow><b>7</b></font>----------------------
|--------<font color=yellow><b>7</b></font>------------<font color=yellow><b>7</b></font>--------------------
|------9----------------9------------------
|----9--------------------9----------------
|-10------------------------10-------------
4  3 3 1 1 1 4  1 1 1 3 3 4
</pre> Guardando le diteggiature noterai che tali note sono suonate con lo stesso dito, esso è usato a mo' di mini-barre. Ad esempio quando suoniamo il Rolling al 9° tasto col dito 3, l'anulare per intenderci, devi fare un mini barrè con la falangetta, in modo da prendere le due corde con lo stesso dito. Similmente, quando arrivi al Rolling al 7° tasto sulle prime 3 corde, usi il dito indice per fare un piccolo barrè su queste corde.
          <br> Ma perché si chiama Rolling e non barrè allora? Il problema sta nel fatto che se tu suoni l'esempio sopra cosi' come abbiamo detto fin'ora non farai altro che suonare un accordo, ovvero quando suoni una corda, quella prima continua a risuonare insieme ad essa e cosi' per tutte quelle successive.
          <br>Ma questo non è uno Sweep, per essere uno Sweep le corde devono suonare
          <b>una alla volta</b>, in pratica l'esempio sopra deve suonare come un arpeggio non come un "accordo apeggiato"!
          <br> Per ottenere questo si deve ruotare leggermente avanti o indietro il dito che si usa come mini barrè, facendo suonare di volta in volta una sola corda e ammutolendo le altre.
          <br> Se hai provato a suonare l'esempio sopra, tutto cio' ti potrà sembrare qualcosa di impossibile e in effetti questo genere di tecnica per essere sviluppata e perfezionata seriamente, specialmente sui movimenti che coinvolgono 5/6 corde, abbisogna di molti anni di appassionato studio ma per ottenere i primi risultati basterà qualche mese.
          <br>Per ottenere l'ammutolimento delle corde che non vogliamo far suonare si ricorre anche al
          <b>Palm Muting</b>, ovvero si ammutoliscono le corde posando leggermente il palmo della mano che tiene il plettro sopra le corde, in prossimità del ponte.
          <br>Oltre a questo c'è anche un'altro accorgimento molto importante da prendere: quando eseguiamo uno sweep su un certo numero di corde, il plettro deve compiere il suo movimento in modo fluido, attraversando le corde con un
          <b>unico movimento</b>, facendo molta attenzione a non far indugiare il plettro quando incontra una corda.
          <br>
          <br> Ricapitolando, quello che devi tenere a mente è:
          <li> Le note devono suonare una alla volta.
          <li> Due note sullo stesso tasto ma su corde diverse le suoni con lo stesso dito, usando il Rolling.
          <li> Il Rolling è il movimento leggermente rotatorio che un dito compie per far risuonare una corda e ammutolire le altre.
          <li> Per ammutolire le corde si usa anche il Palm Muting.
          <li> Il movimento del plettro lungo le corde deve essere fluido e non "scattoso".
