<font size=5>L</font>a Scala Maggiore è di tale importanza che ogni suo grado ha un nome e una funzione precisa che lo caratterizza.<br>
Come visto in precedenza la struttura della Scala Maggiore è: <br>1<sub>+T+</sub>2<sub>+T+</sub>3<sub>+S+</sub>4<sub>+T+</sub>5<sub>+T+</sub>6<sub>+T+</sub>7<sub>+S+</sub>1 o anche 1, 2, 3, 4, 5, 6, 7maj.<br><br>
I nomi che caratterizzano i suoi gradi sono:<br>
<ol type=I>
<li> TONICA
<li> SOPRATONICA
<li> MEDIANTE
<li> SOTTODOMINANTE
<li> DOMINANTE
<li> SOPRADOMINANTE
<li> SENSIBILE
</ol>
<font size=5>D</font>escrizione dei 7 gradi:<br>
<OL TYPE=I>
<LI> TONICA <br> <br>
E' la nota che dà il nome alla scala e ne determina la tonalità, per intenderci è la nota da cui si comincia a contare i toni e i semitoni per costruire gli altri gradi. Di solito è la nota con cui <i>comincia e finisce una melodia</i>.<br><br>
<LI> SOPRATONICA <br><br>
Deriva la sua importanza dal fatto di essere parte dell'accordo di dominante di cui è la quinta, se il secondo grado della scala fosse più alto o più basso di un semitono la scala si troverebbe priva di accordo di dominante il che genererebbe una caratteristica sonorità "oscura" e "misteriosa".<br><br>
<LI> MEDIANTE<br><br> La distanza di questa nota dalla TONICA indica se il modo è maggiore ( 2 toni ) o minore ( 1 tono e 1/2 ). Inoltre fa parte dell'accordo di TONICA di cui naturalmente caratterizza il modo maggiore.
<br><br>
<LI> SOTTODOMINANTE<br><br>
E' una nota parente stretta della TONICA potrebbe essere definita la cugina, insieme alla dominante forma un terzetto molto particolare, esse infatti si richiamano e ricordano l'un l'altra.
<br><br>
<LI> DOMINANTE <br><br>
Essa è la sorella quasi gemella della TONICA, talmente simile da essere la sua antagonista principale sulla scena, in quanto tende ad avere un potere "magnetico" sugli altri gradi paragonabile alla TONICA. Questo infatti è anche il grado su cui si costruisce la scala maggiore più vicina a quella corrente e inoltre fa parte dell'accordo di TONICA.
<br><br>
<LI> SOPRADOMINANTE<br><br>
Essa è una <i>specie</i> di dominante per quanto riguarda il potere che esercita sugli altri gradi. Ogni scala maggiore infatti possiede in sè una scala minore detta Scala Minore Naturale (il termine "naturale" non deve far giungere a nessuna conclusione, l'anno chiamata così e basta) detta anche <i>relativa minore</i> e si dia il caso che la tonica di tale "scala nella scala" sia proprio il nostro sesto grado.<br>
Quindi la scala minore naturale di La, relativa minore di Do è: La Si Do Re Mi Fa Sol.
<br><br>
<LI>SENSIBILE<br><br>
Il settimo grado è cosi' chiamato quando dista dalla tonica un semitono, esso è "il servo" della tonica in quanto ha la caratteristica di far tendere ad essa. Ovvero, quando un brano cade in una sensibile essa tende a dirigere il discorso musicale verso la tonica.
</OL>
