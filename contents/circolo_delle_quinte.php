<font size=5>C</font>ome abbiamo visto in <a href="scale_intervalli_tonalita.html">Scale, Intervalli, Tonalità</a>, le scale non sono altro che degli schemi astratti applicabili a tutte e 12 le note, ovvero a tutte e 12 le tonalità. Spesse volte comunque capita che un pezzo musicale  moduli tonalità, cioè cambi tonalità innalzandola o abbassandola. Ad esempio puo' capitare che un pezzo cominci con una Scala Maggiore di Do, poi si ritrovi in Sol e magari poi ritorni in Do.<br>
Questo tipo di modulazione si dice <i>tonale</i>.<br>
In genere per cambiare tonalità si sceglie una tonalità di arrivo che sia il più vicino possibile a quella di partenza. Per "vicino" si intende una tonalità con il numero di note diverse da quelle della scala di partenza il più basso posibile.<br>Due tonalità diverse hanno solitamente almeno una nota diversa tra loro.<br> Se dobbiamo arrivare ad una tonalità con 3 o 4 note diverse rispetto a quella di partenza, prima moduliamo a quella che ne ha una sola, poi a quella che ne ha due e cosi' via.<br>
Se ad esempio stiamo scrivendo un pezzo in tonalità di Do, Scala Maggiore (Do Re Mi Fa Sol La Si) e vogliamo modulare alla tonalità della Scala Maggiore di Re (Re Mi Fa# Sol La Si Do#), vediamo che le note da modificare sono 2 Fa->Fa# e Do->Do#, percui decidiamo di passare prima dalla Scala Maggiore Di Sol (Sol La Si Do Re Mi Fa# ) cambiando il Fa poi, dopo qualche nota, aggiungiamo il Do# mancante e il gioco è fatto.<br>Se cambiassimo due note contemporaneamente otterremo un effetto alquanto sgradevole per il nostro orecchio.<br>
Ogni tonalità è contraddistinta da un certo numero di diesis e bemolle applicati a determinate note, queste alterazioni vengono poste all'inizio del pentagramma, vengono dette <i>alterazioni in chiave</i> e sono costanti per tutta la durata del pezzo a meno che non venga specificato diversamente.<br>
Ad esempio la tonalità di Do Maggiore ècaratterizzata dal non avere alcuna alterazione, la tonalità di Sol Maggiore ha il <i>Fa# in chiave</i>, la tonalità di Re Maggiore ha il <i>Fa# e il Do# in chiave</i>.<br>
Utilizzando le note di ogni Scala Maggiore è possibile costruire un <i>Scala Minore Naturale</i>, detta anche <i>relativa minore</i>, essa è costruita a partire da un tono 1 e 1/2 sotto la tonica della Scala Maggiore e si denota con una "m" subito dopo il nome della tonalità. Ad esempio la relativa minore della Scala Maggiore di Do è la Scala Minore Naturale di La (Lam).<br><br>

