<h2>Lo Swing</h2>
<font size=5>L</font>o swing è un andamento ritmico classico del Blues, esso ne accentua il senso ansioso/oscillante facendo largo uso di contrattempi sincopi e tempo terzinato.<br>
<br>
<font size=5>L</font>o Swing è caratterizzato da: <br>
