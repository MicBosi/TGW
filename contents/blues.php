<font size=5>I</font>n questa sezione daremo una fugace occhiata al mondo del blues, nessuno pretende di esaurire qui l'argomento, questa sezione è dedicata a coloro che vogliono farsi una idea approssimativa. Appena sarà possibile sostituiro' questa rapida occhiata in un corso più completo.<br>

E' importante sapere qualcosa riguardo al Blues poichè è quello da cui abbiamo ripreso l'uso delle pentatoniche e volenti o nolenti è il seme da cui è nato il ROCK, l'HARD ROCK e l'HEAVY METAL. Le caratteristiche salienti del blues sono:
<OL type=square>
<LI> SCALE PENTATONICHE E BLUES DERIVATE
<LI> DODICI MISURE CON ACCORDI CARATTERISTICI
<LI> Il TEMPO SWING
</OL>
<font size=5>Q</font>ui verranno esplicitate in maniera aridamente sistematica le sue caratteristiche tecniche ma sappiamo bene che per essere dei bravi blues-man e sviscerarne tutte le risorse è necessario riversarci tutto il trasporto di cui siamo capaci.<br>
